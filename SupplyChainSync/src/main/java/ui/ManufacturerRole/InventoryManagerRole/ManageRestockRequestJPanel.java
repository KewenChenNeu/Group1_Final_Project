    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tbProduct.getModel();
        model.setRowCount(0);
        for (WorkRequest req : rmProcurementOrg.getWorkQueue().getWorkRequestList()) {
            if (req instanceof RawMaterialRestockRequest) { 
                RawMaterialRestockRequest rmReq = (RawMaterialRestockRequest) req;
                Object[] row = new Object[7];
                row[0] = rmReq.getRequestId();
                row[1] = rmReq.getMaterial().getMaterialName();
                row[2] = rmReq.getQuantity();
                row[3] = rmReq.getMaterial().getUnit();
                row[4] = rmReq.getQuantity() * rmReq.getMaterial().getUnitPrice();  // total
                row[5] = rmReq.getStatus();
                row[6] = rmReq.getRequestDate();
                model.addRow(row);
            }
        }
    }

