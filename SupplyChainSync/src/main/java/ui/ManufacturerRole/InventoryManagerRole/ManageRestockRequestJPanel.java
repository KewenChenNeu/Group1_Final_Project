    private void btnDeleteRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteRequestActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbProduct.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a request to delete");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tbProduct.getModel();
        int requestId = (int) model.getValueAt(selectedRow, 0); // 假设第0列是 requestId

        WorkRequest toRemove = null;
        for (WorkRequest req : rmProcurementOrg.getWorkQueue().getWorkRequestList()) {
            if (req instanceof RawMaterialRestockRequest) {
                RawMaterialRestockRequest rmReq = (RawMaterialRestockRequest) req;
                if (rmReq.getRequestId() == requestId) {
                    toRemove = rmReq;
                    break;
                }
            }
        }
        if (toRemove != null) {
            rmProcurementOrg.getWorkQueue().getWorkRequestList().remove(toRemove);
            JOptionPane.showMessageDialog(this, "Request deleted successfully!");
            populateTable(); 
        } else {
            JOptionPane.showMessageDialog(this, "Request not found!");
        }
        
    }//GEN-LAST:event_btnDeleteRequestActionPerformed

    private void btnEditQuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuantityActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbProduct.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a request to edit");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tbProduct.getModel();
        int requestId = (int) model.getValueAt(selectedRow, 0); 

        RawMaterialRestockRequest toEdit = null;
        for (WorkRequest req : rmProcurementOrg.getWorkQueue().getWorkRequestList()) {
            if (req instanceof RawMaterialRestockRequest) {
                RawMaterialRestockRequest rmReq = (RawMaterialRestockRequest) req;
                if (rmReq.getRequestId() == requestId) {
                    toEdit = rmReq;
                    break;
                }
            }
        }

        if (toEdit != null) {
            String input = JOptionPane.showInputDialog(this, "Enter new quantity:", toEdit.getQuantity());
            if (input != null && !input.isEmpty()) {
                try {
                    int newQuantity = Integer.parseInt(input);
                    if (newQuantity <= 0) {
                        JOptionPane.showMessageDialog(this, "Quantity must be positive");
                        return;
                    }
                    toEdit.setQuantity(newQuantity);
                    JOptionPane.showMessageDialog(this, "Quantity updated successfully!");
                    populateTable(); 
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Invalid number format");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Request not found!");
        }
    }//GEN-LAST:event_btnEditQuantityActionPerformed


