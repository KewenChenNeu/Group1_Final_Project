    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        UserAccount selectedAccount = getSelectedStaff();
        if (selectedAccount == null) {
            JOptionPane.showMessageDialog(this,
                    "Please select a staff member from the table.",
                    "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Employee emp = selectedAccount.getEmployee();
        StringBuilder details = new StringBuilder();
        details.append("Staff Details\n");
        details.append("================\n\n");
        details.append("Employee ID: ").append(emp.getId()).append("\n");
        details.append("Name: ").append(emp.getName()).append("\n");
        details.append("Email: ").append(emp.getEmail() != null ? emp.getEmail() : "N/A").append("\n");
        details.append("Phone: ").append(emp.getPhone() != null ? emp.getPhone() : "N/A").append("\n\n");
        details.append("Account Information\n");
        details.append("================\n\n");
        details.append("Username: ").append(selectedAccount.getUsername()).append("\n");
        details.append("Role: ").append(selectedAccount.getRole() != null ? selectedAccount.getRole().toString() : "N/A").append("\n");

        JOptionPane.showMessageDialog(this, details.toString(), "Staff Details", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnViewDetailsActionPerformed

