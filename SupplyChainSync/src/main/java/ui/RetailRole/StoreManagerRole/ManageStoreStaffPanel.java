    private void populateStaffTable() {
        DefaultTableModel model = (DefaultTableModel) tblStaff.getModel();
        model.setRowCount(0);

        for (UserAccount account : organization.getUserAccountDirectory().getUserAccountList()) {
            Employee emp = account.getEmployee();
            if (emp != null) {
                Object[] row = new Object[5];
                row[0] = emp.getId();
                row[1] = emp.getName();
                row[2] = account.getRole() != null ? account.getRole().toString() : "N/A";
                row[3] = emp.getEmail() != null ? emp.getEmail() : "N/A";
                row[4] = emp.getPhone() != null ? emp.getPhone() : "N/A";
                model.addRow(row);
            }
        }
    }

    private void updateSummary() {
        int totalStaff = organization.getUserAccountDirectory().getUserAccountList().size();
        txtTotalStaff.setText(String.valueOf(totalStaff));

        int managers = 0;
        int clerks = 0;
        int analysts = 0;

        for (UserAccount account : organization.getUserAccountDirectory().getUserAccountList()) {
            if (account.getRole() != null) {
                String roleName = account.getRole().toString().toLowerCase();
                if (roleName.contains("manager")) {
                    managers++;
                } else if (roleName.contains("clerk")) {
                    clerks++;
                } else if (roleName.contains("analytics") || roleName.contains("analyst")) {
                    analysts++;
                }
            }
        }

        txtManagers.setText(String.valueOf(managers));
        txtClerks.setText(String.valueOf(clerks));
    }

    private UserAccount getSelectedStaff() {
        int selectedRow = tblStaff.getSelectedRow();
        if (selectedRow < 0) {
            return null;
        }

        int empId = (Integer) tblStaff.getValueAt(selectedRow, 0);
        for (UserAccount account : organization.getUserAccountDirectory().getUserAccountList()) {
            if (account.getEmployee() != null && account.getEmployee().getId() == empId) {
                return account;
            }
        }
        return null;
    }

